/* 
OPERADOR === 

O operador estrito de igualdade (===) compara não apenas os valores, mas também os tipos dos operandos. 

*/
var numero = "5";
console.log(typeof numero)

if( numero === 5) {
    console.log("O número é 5")
}

if(numero !== 5) {
    console.log("Não é o número 5 do tipo number");
} 